    private void CodeTextFieldActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_CodeTextFieldActionPerformed
        paymentStatus = paymentStatusData.getPaymentStatus();
        
        if(paymentStatus){
            System.out.println("payment status TRUE");
            paymentStatusData.setPaymentStatus(false);
            clearTable();
            System.out.println("Payment status is True so the table will be cleared first");
        }
        else{
            System.out.println("Payment status is false, proceed as usual");
        }
        String inputcode = CodeTextField.getText();
        
        Item tempItem = null;
        
        int tempIndex = 0;
        for (Item item : itemlist) {
            
            if (item.getKode().equals(inputcode)) {
            
                tempIndex = JumlahBelanja;
                JumlahBelanja++;
                System.out.println("Item Found");
                
                transaksibarang.daftarIdBarang.add(Integer.valueOf(item.getKode()));
                transaksibarang.daftarJumlahBarang.add(JumlahBelanja);
                transaksibarang.daftarHargaBarang.add(item.getHarga());
                
                NameTextField.setText(item.getNama());
                PriceTextField.setText(Float.toString(item.getHarga()));
                
                daftarModel.setValueAt(JumlahBelanja, tempIndex, 0);
                daftarModel.setValueAt(inputcode, tempIndex, 1);
                daftarModel.setValueAt(item.getNama(), tempIndex, 2);
                daftarModel.setValueAt(item.getHarga(), tempIndex, 3);
                daftarModel.setValueAt(1, tempIndex, 4);
                
                
                tempItem = item;
                break;    
            }
        }
        if (tempItem == null) {
            System.out.println("Barang tidak ditemukan!");
            JOptionPane.showMessageDialog(null, "Item with code " + inputcode + " not found.");
        }
        CodeTextField.setText("");
        
    }//GEN-LAST:event_CodeTextFieldActionPerformed

