    private void InputButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_InputButtonActionPerformed
        // TODO add your handling code here:
        DataModel datamodel = new DataModel();
        DataThrown thrown = new DataThrown();
            
            String namaToken = "Token " + datamodel.getNominalToken();
            String namaPulsa = datamodel.getOperatorPulsa() + " " + datamodel.getNominalPulsa();

            Item tempBarang = null;

            int tempIndex = 0;

            for (Item item : itemlist) {
                if (item.getNama().equals(namaToken) || item.getNama().equals(namaPulsa)) {

                    tempIndex = JumlahBelanja;
                    JumlahBelanja++;

                    transaksibarang.daftarIdBarang.add(Integer.valueOf(item.getKode()));
                    transaksibarang.daftarJumlahBarang.add(JumlahBelanja);
                    transaksibarang.daftarHargaBarang.add(item.getHarga());

                    System.out.println("Barang ditemukan!");
                    
                    NameTextField.setText(item.getNama());
                    PriceTextField.setText(Float.toString(item.getHarga()));

                    daftarModel.setValueAt(JumlahBelanja, tempIndex, 0);
                    daftarModel.setValueAt(item.getKode(), tempIndex, 1);
                    daftarModel.setValueAt(item.getNama(), tempIndex, 2);
                    daftarModel.setValueAt(item.getHarga(), tempIndex, 3);
                    daftarModel.setValueAt(1, tempIndex, 4);
                    daftarModel.setValueAt(item.getHarga(), tempIndex, 5);
                    
                    break;
                }
                else{
                    System.out.println("Barang tidak ditemukan!");
                }
                
            }   
            if(namaPulsa!=null){
                datamodel.setNominalPulsa(null);
                datamodel.setOperatorPulsa(null);
                datamodel.setNomorTelepon(null);
            }
            else if(namaToken!= null){
                datamodel.setNominalToken(null);
                datamodel.setNomorMeteran(null);
            }
    }//GEN-LAST:event_InputButtonActionPerformed

    public void clearTable() {
        daftarModel = (DefaultTableModel) daftarTable.getModel();
            
            TableModelListener[] listeners = daftarModel.getTableModelListeners();
            
            
            for(TableModelListener listener : listeners){
                daftarModel.removeTableModelListener(listener);
            }
             
            int rowCount = daftarModel.getRowCount();
            int columnCount = daftarModel.getColumnCount();

            for(int i = 0;i<rowCount;i++){
                for(int j = 0;j<columnCount;j++){
                    daftarModel.setValueAt(null, i, j);
                }
            }
            
            JumlahBelanja = 0;
            transaksibarang.daftarHargaBarang.clear();
            transaksibarang.daftarIdBarang.clear();
            transaksibarang.daftarJumlahBarang.clear();
            
            System.out.println("Table Cleared");
            
            for(TableModelListener listener: listeners){
                daftarModel.addTableModelListener(listener);
            }
    }
